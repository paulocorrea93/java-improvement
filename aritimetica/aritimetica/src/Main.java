public class Main {
    public static void main(String[] args) {
        int a = 5;
        int b = a+5;



        System.out.println("a + b = " + b);

        System.out.println("a = " + a);
        System.out.println("b = " + b);

        System.out.println("a + b = " + (a+b));
        System.out.println("a - b = " + (a-b));
        System.out.println("a x b = " + (a*b));
        System.out.println("a / b = " + (a/b));
        System.out.println("a % b = " + (a%b));
        System.out.println("a++ = " + (a++));
        System.out.println("b++ = " + (b++));
        System.out.println("a-- = " + (a--));
        System.out.println("b-- = " + (b--));
        System.out.println("a+ = " + a);
        System.out.println("a+ = " + b);

        System.out.println("a == b: " + (a==b));
        System.out.println("a != b: " + (a!=b));
        System.out.println("a >= b: " + (a>=b));
        System.out.println("a <= b: " + (a<=b));



    }
}