public class estruturasDeRepeticao {

    public static void main(String[]args){
        for(int i = 0; i <= 5; i++){
            System.out.println("i: " + i);
        }

        int y = 0;
        while(y < 10){
            System.out.println("Working");
            y++;
        }

        int x = 0;

        do{
            System.out.println(x);
            x++;
        }while(x < 10);
    }

}
